// ==================== RECIPE UTILITY FUNCTIONS ====================
export const getRecipesByCategory = (category) => {
  return Object.values(EXPANDED_RECIPES).filter(r => r.category === category);
};

export const getRecipesByTier = (tier) => {
  return Object.values(EXPANDED_RECIPES).filter(r => r.tier === tier);
};

export const getRecipesByRarity = (rarity) => {
  return Object.values(EXPANDED_RECIPES).filter(r => r.rarity === rarity);
};

export const getAvailableRecipes = (chefLevel) => {
  return Object.values(EXPANDED_RECIPES).filter(r => chefLevel >= r.unlockLevel);
};

export const getRecipeStats = () => {
  const recipes = Object.values(EXPANDED_RECIPES);
  return {
    total: recipes.length,
    byTier: {
      1: recipes.filter(r => r.tier === 1).length,
      2: recipes.filter(r => r.tier === 2).length,
      3: recipes.filter(r => r.tier === 3).length,
      4: recipes.filter(r => r.tier === 4).length,
      5: recipes.filter(r => r.tier === 5).length,
    },
    byRarity: {
      common: recipes.filter(r => r.rarity === 'common').length,
      uncommon: recipes.filter(r => r.rarity === 'uncommon').length,
      rare: recipes.filter(r => r.rarity === 'rare').length,
      epic: recipes.filter(r => r.rarity === 'epic').length,
      legendary: recipes.filter(r => r.rarity === 'legendary').length,
    },
    byCategory: Object.keys(RECIPE_CATEGORIES).reduce((acc, cat) => {
      acc[cat.toLowerCase()] = recipes.filter(r => r.category === cat.toLowerCase()).length;
      return acc;
    }, {}),
  };
};

